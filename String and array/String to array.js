var x="Bangladesh";
var y;

// String to single element
y=x.split();
console.log(y);

// String to char array
y=x.split("");
console.log(y);

x="Hello world!"
y=x.split("");
console.log(y);

// splitting based on string or character, removes that string or character
x="Bangladesh is a country";
y=x.split("n");
console.log(y);

y=x.split(" "); // Spilt when space
console.log(y);

