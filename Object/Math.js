var x;

x = Math.PI;
console.log(x);

x = Math.E;
console.log(x);

x = Math.round(3.1);
console.log(x);

x = Math.floor(3.8);
console.log(x);

x = Math.ceil(3.2);
console.log(x);

x = Math.sqrt(69);
console.log(x);

x = Math.abs(-10);
console.log(x);

x = Math.pow(2, 5); // pow(base, exponent);
console.log(x);

x = Math.min(6, 3, 1, 4);
console.log(x);

x = Math.max(6, 3, 1, 4);
console.log(x);

x = Math.random(); //Returns floating numbers between 0 to 1
console.log(x); 

x = Math.random()*100; //returns a value between 0 to 99
console.log(x);

x = Math.random()*100 + 1; //returns a value between 1 to 100
console.log(x);

x = Math.floor(Math.random()*100 + 1); //returns a integer between 1 to 100
console.log(x);


